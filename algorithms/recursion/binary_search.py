def solve(A, n, l, r, num):
    if l <= r:
        pivot = (l + r) // 2
        if A[pivot] == num:
            return pivot + 1
        elif A[pivot] > num:
            r = pivot - 1 
            return solve(A, n, l, r, num)
        elif A[pivot] < num:
            l = pivot + 1
            return solve(A, n, l, r, num)
    else:
        return -1



